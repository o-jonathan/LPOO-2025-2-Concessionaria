    private void btnInfoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInfoActionPerformed
        Veiculo itemSelecionado = lstVeiculo.getSelectedValue();
        if(itemSelecionado != null){
            JOptionPane.showMessageDialog(rootPane, itemSelecionado);
        } else {
            JOptionPane.showMessageDialog(rootPane, "Selecione um item", 
                    "Produto não selecionado", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnInfoActionPerformed

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        CadastroVeiculoJD telaCadastro = new CadastroVeiculoJD(this, true);
        telaCadastro.setVisible(true);

        Veiculo veiculoCad = telaCadastro.getVeiculo();

        listaVeiculos.add(veiculoCad);
        
        loadItensInList();
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        Veiculo itemSelecionado = lstVeiculo.getSelectedValue();
        if(itemSelecionado != null){
            
            CadastroVeiculoJD telaCadastro = new CadastroVeiculoJD(this, true);
            telaCadastro.setVeiculo(itemSelecionado);
            telaCadastro.setVisible(true);
            
            loadItensInList();
            
            
        } else {
            JOptionPane.showMessageDialog(rootPane, "Selecione um item", "Produto não selecionado", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
        Veiculo itemSelecionado = lstVeiculo.getSelectedValue();
        if(itemSelecionado != null){
            
            int remover = JOptionPane.showConfirmDialog(rootPane, 
                    "Tem certeza que deseja excluir o item "+itemSelecionado.getPlaca()+"?");
            
            if(remover == JOptionPane.YES_OPTION){
                listaVeiculos.remove(itemSelecionado);
                loadItensInList();
            }
            
            
        } else {
            JOptionPane.showMessageDialog(rootPane, "Selecione um item", 
                    "Produto não selecionado", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnRemoverActionPerformed

