    public void loadItensInList() {
        DefaultListModel modeloLista = new DefaultListModel();
        modeloLista.removeAllElements();
        for (Veiculo item : listaVeiculos) {
            modeloLista.addElement(item);
        }

        lstVeiculo.setModel(modeloLista);

    }

